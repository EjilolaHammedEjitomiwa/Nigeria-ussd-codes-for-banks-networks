package com.geodeveloper.nigeriaussdcodes;

public class GeneralCodeModel {
    private String mCodeView;
    private String mDescriptionView;


    public GeneralCodeModel(String mCodeView, String mDescriptionView) {
        this.mCodeView = mCodeView;
        this.mDescriptionView = mDescriptionView;
    }

    public String getmCodeView() {
        return mCodeView;
    }

    public String getmDescriptionView() {
        return mDescriptionView;
    }


}
